"""Keyword-routed templated code generator.

Picks one of a small set of Flutter app archetypes based on the idea text
and fills in placeholders. Always available; needs no external services.
"""

from __future__ import annotations

import re

from ..models import GeneratedFile, IdeaSpec, ProjectFiles
from .base import CodeGenerator

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _slug(text: str, fallback: str = "app") -> str:
    s = re.sub(r"[^a-z0-9]+", "_", text.lower()).strip("_")
    return s or fallback


def _title_from_idea(idea: str, app_name: str | None) -> str:
    if app_name:
        return app_name
    # First sentence, capped to ~40 chars.
    head = re.split(r"[.!?\n]", idea, maxsplit=1)[0].strip()
    return head[:40] or "My App"


def _color(hex_or_none: str | None) -> str:
    """Return a Dart `Color(0xFFRRGGBB)` literal."""
    h = (hex_or_none or "#1976D2").lstrip("#")
    return f"Color(0xFF{h.upper()})"


# ---------------------------------------------------------------------------
# Archetype router
# ---------------------------------------------------------------------------

_ARCHETYPE_PATTERNS: list[tuple[str, re.Pattern[str]]] = [
    ("translator", re.compile(r"\btransla(te|tor)\b|siswati|zulu|swahili|english", re.I)),
    ("notes", re.compile(r"\bnotes?\b|todo|task|reminder|journal", re.I)),
    ("calculator", re.compile(r"\bcalc(ulator)?\b|arithmetic|math", re.I)),
    ("flashlight", re.compile(r"\bflash[- ]?light\b|torch", re.I)),
]


def _route(idea: str) -> str:
    for name, pat in _ARCHETYPE_PATTERNS:
        if pat.search(idea):
            return name
    return "default"


# ---------------------------------------------------------------------------
# Generator
# ---------------------------------------------------------------------------


class TemplatedGenerator(CodeGenerator):
    name = "templated"

    def generate(self, spec: IdeaSpec) -> ProjectFiles:
        archetype = _route(spec.idea)
        title = _title_from_idea(spec.idea, spec.app_name)
        package = _slug(title, fallback="my_app")
        color = _color(spec.primary_color)

        files: list[GeneratedFile] = []

        # Common project files
        files.append(GeneratedFile(path="pubspec.yaml", content=_pubspec(package)))
        files.append(GeneratedFile(path=".gitignore", content=_FLUTTER_GITIGNORE))
        files.append(
            GeneratedFile(
                path="README.md",
                content=_readme(title, archetype, spec.idea),
            )
        )
        files.append(
            GeneratedFile(
                path="analysis_options.yaml",
                content=_ANALYSIS_OPTIONS,
            )
        )

        # Archetype-specific lib/main.dart
        if archetype == "translator":
            main = _MAIN_TRANSLATOR
        elif archetype == "notes":
            main = _MAIN_NOTES
        elif archetype == "calculator":
            main = _MAIN_CALCULATOR
        elif archetype == "flashlight":
            main = _MAIN_FLASHLIGHT
        else:
            main = _MAIN_DEFAULT

        main = main.replace("__APP_TITLE__", title).replace("__PRIMARY_COLOR__", color)
        files.append(GeneratedFile(path="lib/main.dart", content=main))

        return ProjectFiles(archetype=archetype, files=files)


# ---------------------------------------------------------------------------
# Static templates
# ---------------------------------------------------------------------------


def _pubspec(package: str) -> str:
    return f"""name: {package}
description: Generated by Auto AI App Builder.
publish_to: 'none'
version: 0.1.0+1

environment:
  sdk: ">=3.0.0 <4.0.0"

dependencies:
  flutter:
    sdk: flutter
  cupertino_icons: ^1.0.6

dev_dependencies:
  flutter_test:
    sdk: flutter
  flutter_lints: ^3.0.0

flutter:
  uses-material-design: true
"""


def _readme(title: str, archetype: str, idea: str) -> str:
    return f"""# {title}

Generated by **Auto AI App Builder** from the idea:

> {idea}

Archetype: `{archetype}`.

## Build

```bash
flutter pub get
flutter build apk --release
```
"""


_FLUTTER_GITIGNORE = """.dart_tool/
.flutter-plugins
.flutter-plugins-dependencies
.packages
.pub-cache/
.pub/
build/
*.iml
local.properties
"""

_ANALYSIS_OPTIONS = """include: package:flutter_lints/flutter.yaml

linter:
  rules:
    prefer_const_constructors: true
    avoid_print: false
"""


