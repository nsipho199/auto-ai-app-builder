_MAIN_CALCULATOR = """import 'package:flutter/material.dart';

void main() => runApp(const CalcApp());

class CalcApp extends StatelessWidget {
  const CalcApp({super.key});
  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      title: '__APP_TITLE__',
      theme: ThemeData(colorSchemeSeed: const __PRIMARY_COLOR__, useMaterial3: true),
      home: const CalcHome(),
    );
  }
}

class CalcHome extends StatefulWidget {
  const CalcHome({super.key});
  @override
  State<CalcHome> createState() => _CalcHomeState();
}

class _CalcHomeState extends State<CalcHome> {
  String _expr = '';
  String _result = '0';

  static const _keys = [
    ['7', '8', '9', '/'],
    ['4', '5', '6', '*'],
    ['1', '2', '3', '-'],
    ['0', '.', '=', '+'],
  ];

  void _onKey(String k) {
    setState(() {
      if (k == '=') {
        _result = _evaluate(_expr);
      } else {
        _expr += k;
      }
    });
  }

  String _evaluate(String e) {
    // Tiny shunting-yard for + - * /
    final tokens = RegExp(r'(\\d+\\.?\\d*|[+\\-*/])').allMatches(e).map((m) => m.group(0)!).toList();
    final ops = <String>[];
    final out = <String>[];
    int prec(String op) => (op == '*' || op == '/') ? 2 : 1;
    for (final t in tokens) {
      if (RegExp(r'^\\d').hasMatch(t)) {
        out.add(t);
      } else {
        while (ops.isNotEmpty && prec(ops.last) >= prec(t)) {
          out.add(ops.removeLast());
        }
        ops.add(t);
      }
    }
    while (ops.isNotEmpty) {
      out.add(ops.removeLast());
    }
    final stack = <double>[];
    for (final t in out) {
      if (RegExp(r'^\\d').hasMatch(t)) {
        stack.add(double.parse(t));
      } else if (stack.length >= 2) {
        final b = stack.removeLast();
        final a = stack.removeLast();
        switch (t) {
          case '+': stack.add(a + b); break;
          case '-': stack.add(a - b); break;
          case '*': stack.add(a * b); break;
          case '/': stack.add(b == 0 ? double.nan : a / b); break;
        }
      }
    }
    if (stack.isEmpty) return '0';
    final v = stack.last;
    return v == v.roundToDouble() ? v.toInt().toString() : v.toString();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(title: const Text('__APP_TITLE__')),
      body: Column(
        children: [
          Container(
            alignment: Alignment.centerRight,
            padding: const EdgeInsets.all(16),
            child: Column(
              crossAxisAlignment: CrossAxisAlignment.end,
              children: [
                Text(_expr, style: const TextStyle(fontSize: 24, color: Colors.grey)),
                Text(_result, style: const TextStyle(fontSize: 48)),
              ],
            ),
          ),
          Expanded(
            child: Column(
              children: _keys
                  .map(
                    (row) => Expanded(
                      child: Row(
                        children: row
                            .map((k) => Expanded(
                                  child: Padding(
                                    padding: const EdgeInsets.all(6),
                                    child: FilledButton(
                                      onPressed: () => _onKey(k),
                                      child: Text(k, style: const TextStyle(fontSize: 24)),
                                    ),
                                  ),
                                ))
                            .toList(),
                      ),
                    ),
                  )
                  .toList(),
            ),
          ),
        ],
      ),
    );
  }
}
"""

